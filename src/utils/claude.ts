        '- 良い変化・悪い変化という評価をするな。変化を描写しろ',
        '- 過去の日記がない時点は正直に「この時期の日記がない」と書け。想像で埋めるな',
        '- 変化がない部分は「変わっていない」と書いていい。すべてが変わっている必要はない',
        '- 登山メタファーは使うな',
        '- 日記に書かれていない出来事を捏造するな',
