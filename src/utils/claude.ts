        '【主語を間違えるな】',
        '- 書き手が他者に言った言葉の内容は、書き手自身の内面ではない',
        '- 他者の行動を書き手の行動として読むな',
        '',
        '【変化の読み方】',
        '変化とは：',
        '- 同じ話題への態度・温度が変わった',
